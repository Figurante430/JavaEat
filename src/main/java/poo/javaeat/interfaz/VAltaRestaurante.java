    /**Rellena la tabla con todas las comidas disponibles
     * 
     */
    public void rellenaTabla() {
        limpiaTabla();
        comidas=ClaseUtil.getComidas();
        for (Comida com : comidas) {
            Boolean Marca=false; // para saber si la comida esta dentro del restaurante y por tanto no la metenmos en la tabla
            for (Comida c :comSelecc){
                if (com.getTitulo().equals(c.getTitulo())){
                    Marca=true;
                }
            }
            if (!Marca){dtm.addRow(new Object [] {com.getTitulo(),com.getIngredientes(),com.getPrecio()});}
        }
    }
    
    /**Rellena la tabla con todas las comidas del restaurante
     * 
     */
    public void rellenaTablaComSele(){
        limpiaTabla1();
        comidas=comSelecc;
        for (Comida com : comidas) {
            dtm1.addRow(new Object [] {com.getTitulo(),com.getIngredientes(),com.getPrecio()});
        }
    } 
    
    
    public void limpiaTabla() {
        int filas = dtm.getRowCount();
        for (int i = 0; i < filas; i++) {
            dtm.removeRow(0);
        }   
    }
    public void limpiaTabla1() {
        int filas = dtm1.getRowCount();
        for (int i = 0; i < filas; i++) {
            dtm1.removeRow(0);
        }   
    }
