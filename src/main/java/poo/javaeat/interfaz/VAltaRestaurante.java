    /**Das de alta un restaurante 
     * 
     * @param evt 
     */
    private void altaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altaButtonActionPerformed
        try{
            String nombre =nameText.getText();
            String cif = cifText.getText();
            Boolean catering = catButton.isSelected();
            Double gastosEnvio = Double.parseDouble(gastText.getText());
            Double calificacion = Double.parseDouble(caliText.getText());
            Integer tiempoE = Integer.parseInt(enviText.getText());
            String calle = calleText.getText();
            Integer numero = Integer.parseInt(numberText.getText());
            String cp =cpText.getText();
            String ciudad = cityText.getText();
            String Espe="NINGUNO";
            Direccion d = new Direccion (calle, numero, cp, ciudad);
            if (mejiButton.isSelected()){ Espe = "COMIDA MEJICANA";}
            else if (caseButton.isSelected()){Espe = "COMIDA CASERA";}
            else if (asiaButton.isSelected()){Espe = "COMIDA ASIATICA";}
            else if (indiButton.isSelected()){Espe = "COMIDA INDIA";}
            Restaurante res = new Restaurante (nombre,cif,d,Espe,gastosEnvio,tiempoE,catering,comSelecc);
            ClaseUtil.altaRestaurante(res);
            ClaseUtil.guardarDatos();
            this.dispose();
        }
        catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Excepción al guardar cambios, compruebe los datos", "Mensaje", JOptionPane.ERROR_MESSAGE);}
    }//GEN-LAST:event_altaButtonActionPerformed

