    /**Elimina una comida del array del restaurante
     * 
     * @param evt 
     */
    private void elButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elButtonActionPerformed
        // TODO add your handling code here:
        try{
            int fila = jTable2.getSelectedRow();
            int columna = 0;
            Object value = jTable2.getValueAt(fila, columna);
            String nombre= (String) value;
            System.out.println("MARCADOR: "+nombre);
            ArrayList<Comida> copia= new ArrayList<>(res.getComidas());
            for (Comida com : copia){
                if (com.getTitulo().equals(nombre)){
                    if (res.getComidas().contains(com)){
                        res.moveComida(com);
                    }
                }
            }
            System.out.println(res.getComidas());
            rellenaTablaComSele();
            rellenaTabla();
        }
        catch(ArrayIndexOutOfBoundsException et) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila ");
        }
        catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila ");
        }
        
    }//GEN-LAST:event_elButtonActionPerformed

