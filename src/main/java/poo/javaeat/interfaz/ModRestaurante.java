    /**Confirma la modificacion en el restaurante
     * 
     * @param evt 
     */
    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        try{
            /** Seteamos el restaurante con sus nuevos datos*/
            res.setNombre(nameText.getText());
            res.setCIF(cifText.getText());
            res.setCatering(catButton.isSelected());
            Double gastosEnvio = Double.parseDouble(gastText.getText());
            res.setGastosEnvio(gastosEnvio);
            Double calificacion = Double.parseDouble(caliText.getText());
            res.setCalificacion(calificacion);
            Integer tiempoE = Integer.parseInt(enviText.getText());
            res.setTiempoEspera(tiempoE);
            String calle = calleText.getText();
            res.getDir().setCalle(calle);
            Integer numero = Integer.parseInt(numberText.getText());
            res.getDir().setNumero(numero);
            String cp =cpText.getText();
            res.getDir().setCP(cp);
            String ciudad = cityText.getText();
            res.getDir().setCiudad(ciudad);
            if (mejiButton.isSelected()){
                String Espe = "COMIDA MEJICANA";
                res.setEspecialidad(Espe);
                }
            else if (caseButton.isSelected()){String Espe = "COMIDA CASERA";
                res.setEspecialidad(Espe);
}
            else if (asiaButton.isSelected()){String Espe = "COMIDA ASIATICA";
                res.setEspecialidad(Espe);
}
            else if (indiButton.isSelected()){String Espe = "COMIDA INDIA";
                res.setEspecialidad(Espe);
}       
            JOptionPane.showMessageDialog(this, "Restaurante Modificado", "MODIFICACION REALIZADA", JOptionPane.OK_OPTION);
            ClaseUtil.guardarDatos();
            this.dispose();
        }
        catch (HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Excepción al modificar, compruebe los datos", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_confirmButtonActionPerformed

    /** Introduce la comida en el restaurante si no lo esta ya
     * 
     * @param evt 
     */
    private void selecButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecButtonActionPerformed
        // TODO add your handling code here:
        try{
            int fila = jTable1.getSelectedRow();
            int columna = 0;
            Object value = jTable1.getValueAt(fila, columna);
            String nombre= (String) value;
            ArrayList<Comida> copia= new ArrayList<>(ClaseUtil.getComidas());
            ArrayList<Comida> copia1= new ArrayList<>(res.getComidas());
            for (Comida com : copia){
                if (com.getTitulo().equals(nombre)&&(!copia1.contains(com))){
                    res.getComidas().add(com);
                    System.out.println(res.getComidas().toString());}
            }
            rellenaTabla();
            rellenaTablaComSele();
            ClaseUtil.guardarDatos();
        }
        catch(ArrayIndexOutOfBoundsException et) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila ");
        }
        catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila ");
        }  
    }//GEN-LAST:event_selecButtonActionPerformed

