    private void verUsuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verUsuButtonActionPerformed
        try{
            int fila = jTable1.getSelectedRow();        
            Cliente cliente = usuarios.get(fila);
            if (cliente.getClass().getSimpleName().equals("Empresa")){
                vcVerUsu v= new vcVerUsu(this,true,(Empresa) cliente); 
            }
            else {vcVerUsu v= new vcVerUsu(this,true,(Particular)cliente);
            }
            rellenaTabla();
        }
        catch(ArrayIndexOutOfBoundsException et) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila ");
        }
        catch (IndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila ");
        }
    }//GEN-LAST:event_verUsuButtonActionPerformed
