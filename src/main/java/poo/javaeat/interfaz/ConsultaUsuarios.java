    public void rellenaTabla() {
        limpiaTabla();
        usuarios=ClaseUtil.getUsuarios();
        for (Cliente usu : usuarios) {
            dtm.addRow(new Object [] {usu.getNombre(),usu.getTelf(),usu.getClave(),usu.getCorreo()});
        }
    }
    
    public void limpiaTabla() {
        int filas = dtm.getRowCount();
        for (int i = 0; i < filas; i++) {
            dtm.removeRow(0);
        }   
    }

